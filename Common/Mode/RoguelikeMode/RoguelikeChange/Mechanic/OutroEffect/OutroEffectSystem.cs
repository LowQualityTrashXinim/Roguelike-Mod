		Add_PistolTag();
		Add_RifleTag();
		Add_ShotgunTag();
		Add_LauncherTag();
		Add_MagicStaffTag();
		Add_MagicWandTag();
		Add_MagicBookTag();
		Add_MagicGunTag();
		Add_OtherTag();
		watch.Stop();
		Mod.Logger.Info("Time taken to initialize tag: " + watch.ToString());
	}
